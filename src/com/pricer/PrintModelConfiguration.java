package com.pricer;

public class PrintModelConfiguration {

	private String ID;
	private String PRINT_HOST_ID;
	private String DEFAULT_MODEL;
	private String MODEL;
	private String PRINTER;
	private String STICKERSIZE_ID;
	
	
	
	public String getID() 									{	return ID;							}
	public void setID(String iD) 							{	ID = iD;							}
	
	public String getPRINT_HOST_ID() 						{	return PRINT_HOST_ID;				}
	public void setPRINT_HOST_ID(String pRINT_HOST_ID) 		{	PRINT_HOST_ID = pRINT_HOST_ID;		}
	
	public String getDEFAULT_MODEL() 						{	return DEFAULT_MODEL;				}
	public void setDEFAULT_MODEL(String dEFAULT_MODEL) 		{	DEFAULT_MODEL = dEFAULT_MODEL;		}
	
	public String getMODEL() 								{	return MODEL;						}
	public void setMODEL(String mODEL) 						{	MODEL = mODEL;						}
	
	public String getPRINTER() 								{	return PRINTER;						}
	public void setPRINTER(String pRINTER) 					{	PRINTER = pRINTER;					}
	
	public String getSTICKERSIZE_ID() 						{	return STICKERSIZE_ID;				}
	public void setSTICKERSIZE_ID(String sTICKERSIZE_ID)	{	STICKERSIZE_ID = sTICKERSIZE_ID;	}
	
	
	
	
	
	
}
