package com.pricer;

public class PrintHostConfiguration {
	
	
	private String ID;
	private String HOSTNAME;
	private String SUBCELL;
	private String ALIAS;
	
	
	public String getID() 									{	return ID;		}
	public void setID(String iD) 							{	ID = iD;		}
	
	
	public String getHOSTNAME() 							{	return HOSTNAME;		}
	public void setHOSTNAME(String hOSTNAME) 				{	HOSTNAME = hOSTNAME;	}
	
	
	
	public String getSUBCELL() 								{	return SUBCELL;			}
	public void setSUBCELL(String sUBCELL) 					{	SUBCELL = sUBCELL;		}
	
	public String getALIAS() 								{	return ALIAS;						}
	public void setALIAS(String aLIAS) 						{	ALIAS = aLIAS;						}
			
	

}
